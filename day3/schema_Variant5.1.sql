DROP DATABASE IF EXISTS Hotel_Variant5;
CREATE DATABASE Hotel_Variant5;
USE Hotel_Variant5;

-- 1. Создание таблиц
CREATE TABLE Номера (
    id_номера INT PRIMARY KEY AUTO_INCREMENT,
    тип VARCHAR(50) NOT NULL,
    цена_за_сутки DECIMAL(10,2) NOT NULL,
    вместимость INT NOT NULL,
    статус VARCHAR(20) DEFAULT 'Свободен'
);

CREATE TABLE Гости (
    id_гостя INT PRIMARY KEY AUTO_INCREMENT,
    фамилия VARCHAR(50) NOT NULL,
    имя VARCHAR(50) NOT NULL,
    отчество VARCHAR(50),
    паспорт VARCHAR(20) UNIQUE NOT NULL,
    телефон VARCHAR(20)
);

CREATE TABLE Бронирования (
    id_бронирования INT PRIMARY KEY AUTO_INCREMENT,
    id_гостя INT NOT NULL,
    id_номера INT NOT NULL,
    дата_заезда DATE NOT NULL,
    дата_выезда DATE NOT NULL,
    статус VARCHAR(20),
    FOREIGN KEY (id_гостя) REFERENCES Гости(id_гостя),
    FOREIGN KEY (id_номера) REFERENCES Номера(id_номера)
);

CREATE TABLE Услуги (
    id_услуги INT PRIMARY KEY AUTO_INCREMENT,
    название VARCHAR(100) NOT NULL,
    цена DECIMAL(10,2) NOT NULL
);

CREATE TABLE Доп_услуги_бронирования (
    id_бронирования INT NOT NULL,
    id_услуги INT NOT NULL,
    количество INT NOT NULL,
    PRIMARY KEY (id_бронирования, id_услуги),
    FOREIGN KEY (id_бронирования) REFERENCES Бронирования(id_бронирования),
    FOREIGN KEY (id_услуги) REFERENCES Услуги(id_услуги)
);

